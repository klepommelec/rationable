export const EmojiPicker: React.FC<EmojiPickerProps> = ({ emoji, setEmoji }) => {
  const [isOpen, setIsOpen] = React.useState(false);
  const [searchTerm, setSearchTerm] = React.useState('');

  const filteredEmojis = React.useMemo(() => {
    if (!searchTerm) return EMOJI_CATEGORIES;
    
    return EMOJI_CATEGORIES.map(category => ({
      ...category,
      emojis: category.emojis.filter(e => 
        e.includes(searchTerm) || 
        category.name.toLowerCase().includes(searchTerm.toLowerCase())
      )
    })).filter(category => category.emojis.length > 0);
  }, [searchTerm]);

  const handleEmojiSelect = (selectedEmoji: string) => {
    setEmoji(selectedEmoji);
    setIsOpen(false);
    setSearchTerm('');
  };

  return (
    <Popover open={isOpen} onOpenChange={setIsOpen}>
      <PopoverTrigger asChild>
        <Button variant="ghost" size="icon" className="text-4xl w-14 h-14 rounded-full hover:bg-white/10 shrink-0">
          {emoji}
        </Button>
      </PopoverTrigger>
      <PopoverContent className="w-96 p-0 bg-slate-800 border-slate-700" align="start">
        <div className="p-3 border-b border-slate-700">
          <div className="relative">
            <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 h-4 w-4 text-slate-400" />
            <Input
              placeholder="Rechercher un emoji..."
              value={searchTerm}
              onChange={(e) => setSearchTerm(e.target.value)}
              className="pl-10 bg-slate-700 border-slate-600 text-white placeholder-slate-400"
            />
          </div>
        </div>
        
        <Tabs defaultValue="Populaires" className="w-full">
          <TabsList className="grid w-full grid-cols-4 bg-slate-700/50">
            {filteredEmojis.slice(0, 4).map((category) => (
              <TabsTrigger 
                key={category.name} 
                value={category.name}
                className="text-xs data-[state=active]:bg-slate-600"
              >
                {category.name}
              </TabsTrigger>
            ))}
          </TabsList>
          
          <div className="max-h-64 overflow-y-auto">
            {filteredEmojis.map((category) => (
              <TabsContent key={category.name} value={category.name} className="mt-0">
                <div className="p-3">
                  <div className="grid grid-cols-8 gap-1">
                    {category.emojis.map((e, index) => (
                      <Button
                        key={`${e}-${index}`}
                        variant="ghost"
                        size="icon"
                        className="text-2xl h-10 w-10 rounded-md hover:bg-slate-700 transition-colors"
                        onClick={() => handleEmojiSelect(e)}
                        title={e}
                      >
                        {e}
                      </Button>
                    ))}
                  </div>
                </div>
              </TabsContent>
            ))}
          </div>
          
          {filteredEmojis.length > 4 && (
            <TabsList className="grid w-full grid-cols-3 bg-slate-700/50 border-t border-slate-600">
              {filteredEmojis.slice(4, 7).map((category) => (
                <TabsTrigger 
                  key={category.name} 
                  value={category.name}
                  className="text-xs data-[state=active]:bg-slate-600"
                >
                  {category.name}
                </TabsTrigger>
              ))}
            </TabsList>
          )}
        </Tabs>
      </PopoverContent>
    </Popover>
  );
};
